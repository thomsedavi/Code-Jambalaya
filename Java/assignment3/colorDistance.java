package assignment3;

import java.awt.Color;

public class colorDistance {
	
	private float z;
	private Color color;
	
	public colorDistance(float z, Color color) {
		this.z = z;
		this.color = color;
	}
	
	public float getZ() {
		return z;
	}
	
	public Color getColor() {
		return color;
	}

}
